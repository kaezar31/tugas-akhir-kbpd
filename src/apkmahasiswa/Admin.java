    private void data(){
     try{
            con = Koneksi.getKoneksi();
            String sql1 = "SELECT * FROM mahasiswa order by id ASC";
            st = con.createStatement();
            rs = st.executeQuery(sql1);
            int no = 0;
            while(rs.next()){
                DefaultTableModel model = (DefaultTableModel) TableMahasiswa.getModel();
                Object[] row = new Object[10];
                row[0] = no = no + 1;
                row[1] = rs.getString("nama");
                row[2] = rs.getString("npm");
                row[3] = rs.getString("prodi");  
                row[4] = rs.getString("jurusan");  
                row[5] = rs.getString("kelas") + " - " + rs.getString("semester");
                double DPK1;
                DPK1 = Double.parseDouble(rs.getString("dpk"));
                String Sisa = Integer.toString(6000000 - rs.getInt("dpk"));
                double Sisa2 = Double.parseDouble(Sisa); 
                DecimalFormat DF = new DecimalFormat("#,###,###");        
                String DPK = DF.format(DPK1);
                String Sisa3 = DF.format(Sisa2);
                row[6] = DPK;
                row[7] = Sisa3;
                String status = "";
                if (Integer.parseInt(rs.getString("dpk")) == 6000000) {
                    status = "Lunas";
                }else {
                    status = "Belum Lunas";
                }
                row[8] = status;
                row[9] = rs.getInt("id");
                model.addRow(row);
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }


