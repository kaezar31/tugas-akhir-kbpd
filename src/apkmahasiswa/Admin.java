    private void TableMahasiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableMahasiswaMouseClicked
        btnSimpan.setEnabled(false);
        btnEdit.setEnabled(true);
        btnHapus.setEnabled(true);

        int i = TableMahasiswa.getSelectedRow();
        TableModel model = TableMahasiswa.getModel();

        String kelasSemester = model.getValueAt(i, 5).toString();
        String noSpaceStr = kelasSemester.replaceAll("\\s", "");
        String[] kata = noSpaceStr.split("-");
        String kelas = kata[0];
        String jurusan = kata[1];

        inputNama.setText(model.getValueAt(i, 1).toString());
        inputNPM.setText(model.getValueAt(i, 2).toString());
        inputProdi.setSelectedItem(model.getValueAt(i, 3).toString());
        inputJurusan.setSelectedItem(model.getValueAt(i, 4).toString());
        inputKelas.setText(kelas);
        inputSemester.setText(jurusan);
        inputDPK.setText(model.getValueAt(i, 6).toString());
        hiddenID.setText(model.getValueAt(i, 9).toString());
    }//GEN-LAST:event_TableMahasiswaMouseClicked

