    private void inputProdiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inputProdiActionPerformed
        if(inputProdi.getSelectedItem().equals("Hukum")){
            inputJurusan.removeAllItems();
            inputJurusan.addItem("Ilmu Hukum S1");
        }else if(inputProdi.getSelectedItem().equals("Ekonomi")){
            inputJurusan.removeAllItems();
            inputJurusan.addItem("Manajemen S1");
            inputJurusan.addItem("Akuntansi S1");

        }else if(inputProdi.getSelectedItem().equals("Teknik")){
            inputJurusan.removeAllItems();
            inputJurusan.addItem("Teknik Informatika S1");
            inputJurusan.addItem("Teknik Sipil S1");
            inputJurusan.addItem("Teknik Elektro S1");
            inputJurusan.addItem("Teknik Industri S1");
            inputJurusan.addItem("Arsitektur S1");

        }else if(inputProdi.getSelectedItem().equals("FISIP")){
            inputJurusan.removeAllItems();
            inputJurusan.addItem("Ilmu Pemerintahan S1");
            inputJurusan.addItem("Ilmu Komunikasi S1");
            inputJurusan.addItem("Kesejahteraan Sosial S1");
            inputJurusan.addItem("Kepolisian D3");

        }else if(inputProdi.getSelectedItem().equals("FKIP")){
            inputJurusan.removeAllItems();
            inputJurusan.addItem("Pendidikan Ekonomi S1");
            inputJurusan.addItem("Pendidikan Matematika S1");
            inputJurusan.addItem("Pendidikan Guru SD S1");
        }
    }//GEN-LAST:event_inputProdiActionPerformed

